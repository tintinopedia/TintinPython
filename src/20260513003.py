# Section C: IF ELSE Practice
# If number is even → print Even else print Odd
number = int(input("Enter a number:"))
if number % 2 == 0:
    print("Even")
else:
    print("Odd")


# If age ≥ 18 → print You can vote else print You cannot vote
age = float(input("Enter your age:"))
if age >= 18:
    print("You can vote")
else:
    print("You cannot vote.")

